let marks = [10, 20, 40, 50, 60]

document.writeln(marks)

document.writeln("<h3>After pushing 30</h3>")

marks.push(30)

document.writeln(marks)

document.writeln("<h3>After popping 30</h3>")

marks.pop(30)

document.writeln(marks)

document.writeln("<h3>After deleting item at index 0</h3>")

delete marks[0]

document.writeln(marks)